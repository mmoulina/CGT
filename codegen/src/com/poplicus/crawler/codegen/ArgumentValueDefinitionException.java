package com.poplicus.crawler.codegen;

public class ArgumentValueDefinitionException extends Exception {

	private static final long serialVersionUID = 8527236372045404168L;

	public ArgumentValueDefinitionException() {
		super();
	}

	public ArgumentValueDefinitionException(String message) {
		super(message);
	}
	
	public ArgumentValueDefinitionException(String message, Throwable cause) {
		super(message, cause);
	}
	
}