package com.poplicus.crawler.codegen;

public class IfElseStatementException extends Exception {

	private static final long serialVersionUID = -209555132094725209L;

	public IfElseStatementException() {
		super();
	}

	public IfElseStatementException(String message) {
		super(message);
	}
	
	public IfElseStatementException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
