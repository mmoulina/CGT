package com.poplicus.crawler.codegen;

public class MethodDefinitionException extends Exception {

	private static final long serialVersionUID = -8404840223413097806L;

	public MethodDefinitionException() {
		super();
	}

	public MethodDefinitionException(String message) {
		super(message);
	}
	
	public MethodDefinitionException(String message, Throwable cause) {
		super(message, cause);
	}
	
}